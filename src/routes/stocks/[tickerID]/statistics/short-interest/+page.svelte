<script lang="ts">
  import { displayCompanyName, stockTicker } from "$lib/store";
  import { abbreviateNumber, removeCompanyStrings } from "$lib/utils";
  import SEO from "$lib/components/SEO.svelte";
  import Infobox from "$lib/components/Infobox.svelte";
  import Tutorial from "$lib/components/Tutorial.svelte";
  import TableHeader from "$lib/components/Table/TableHeader.svelte";
  import { onMount } from "svelte";
  import DownloadData from "$lib/components/DownloadData.svelte";

  //import * as XLSX from 'xlsx';
  import highcharts from "$lib/highcharts.ts";
  import { mode } from "mode-watcher";

  export let data;

  let rawData = data?.getData?.history || [];
  let tableList = rawData;
  tableList
    ?.sort((a, b) => new Date(b?.recordDate) - new Date(a?.recordDate))
    ?.slice(0, 20);

  async function handleScroll() {
    const scrollThreshold = document.body.offsetHeight * 0.8; // 80% of the website height
    const isBottom = window.innerHeight + window.scrollY >= scrollThreshold;
    if (isBottom && tableList?.length !== rawData?.history?.length) {
      const nextIndex = tableList?.length;
      const newResults = rawData?.slice(nextIndex, nextIndex + 20);
      tableList = [...tableList, ...newResults];
    }
  }

  let steps = [
    {
      popover: {
        title: "Short Interest",
        description:
          "This dashboard tracks how many shares are sold short and key ratios that traders use to spot bearish sentiment and potential squeezes.",
        side: "center",
        align: "center",
      },
    },
    {
      element: ".short-interest-driver",
      popover: {
        title: "Total Short Interest",
        description: `The total number of shares currently sold short (${abbreviateNumber(data?.getData?.sharesShort)} here). A rising number can indicate growing bearish bets.`,
        side: "left",
        align: "start",
      },
    },
    {
      element: ".shortPriorMonth-driver",
      popover: {
        title: "Last Month’s Shorts",
        description: (() => {
          const priorShorts = abbreviateNumber(
            data?.getData?.sharesShortPriorMonth,
          );
          const change = tableList?.at(0)?.percentChangeMoMo;
          const momentumDirection =
            change >= 0 ? "momentum—up" : "momentum—down";

          let jumpSize = "a small jump";
          const absChange = Math.abs(change);

          if (absChange >= 20) {
            jumpSize = "a big jump";
          } else if (absChange >= 10) {
            jumpSize = "a medium jump";
          }

          return `Compare to last month’s short interest (${priorShorts}) to see ${momentumDirection}. ${Math.abs(change)}% MoM means ${jumpSize} in bearish positioning.`;
        })(),
        side: "bottom",
        align: "start",
      },
    },
    {
      element: ".changeMoM-driver",
      popover: {
        title: "Month‑over‑Month Change",
        description: `"Shows the % change from last month (${tableList?.at(0)?.percentChangeMoMo}%). Large swings can precede volatility or squeeze setups.`,
        side: "bottom",
        align: "start",
      },
    },
    {
      element: ".shortPercentFloat-driver",
      popover: {
        title: "Short % of Float",
        description: `${data?.getData?.shortFloatPercent}% tells you what portion of the freely tradable shares are sold short. The higher it is, the more crowded the short side.`,
        side: "left",
        align: "start",
      },
    },
    {
      element: ".shortPercentOutstanding-driver",
      popover: {
        title: "Short % of Outstanding",
        description: `${data?.getData?.shortOutstandingPercent}% shows shorts relative to all shares out there. Use alongside % float to understand supply dynamics.`,
        side: "left",
        align: "start",
      },
    },
    {
      element: ".daysToCover-driver",
      popover: {
        title: "Days to Cover",
        description: `${data?.getData?.shortRatio} days — how long it’d take shorts to buy back at average daily volume. Low values can fuel quick squeezes.`,
        side: "right",
        align: "start",
      },
    },
    {
      element: ".chart-driver",
      popover: {
        title: "Visualizing Trends",
        description:
          "This chart overlays price, % float, and % outstanding over time so you can spot divergences or spikes.",
        side: "right",
        align: "start",
      },
    },
    {
      element: ".history-driver",
      popover: {
        title: "Detailed History",
        description:
          "Scroll through past filings to see how metrics evolved—handy. You can also download the data for backtesting your entry and exit rules.",
        side: "right",
        align: "start",
      },
    },
    {
      popover: {
        title: "You’re All Set!",
        description:
          "Now you know how to read each short‑interest metric and apply it to your trading strategies. Happy investing!",
        side: "center",
        align: "center",
      },
    },
  ];

  function plotData() {
    if (!rawData || rawData.length === 0) {
      return {};
    }

    // Prepare series data in one pass
    const priceSeries = [];
    const shortFloatData = [];

    rawData.forEach(({ recordDate, price, shortPercentOfFloat }) => {
      const time = new Date(recordDate).getTime();
      priceSeries.push([time, price]);
      shortFloatData.push({ time, price, shortFloat: shortPercentOfFloat });
    });

    // Calculate statistical thresholds for categorization
    const shortFloatValues = shortFloatData.map((d) => d.shortFloat);
    const maxShortFloat = Math.max(...shortFloatValues);
    const minShortFloat = Math.min(...shortFloatValues);
    const avgShortFloat =
      shortFloatValues.reduce((sum, val) => sum + val, 0) /
      shortFloatValues.length;

    // Calculate standard deviation for better threshold detection
    const variance =
      shortFloatValues.reduce(
        (sum, val) => sum + Math.pow(val - avgShortFloat, 2),
        0,
      ) / shortFloatValues.length;
    const stdDev = Math.sqrt(variance);

    // Define thresholds for different severity levels (relative to avg)
    const extremeThreshold = avgShortFloat + 2 * stdDev;
    const highThreshold = avgShortFloat + stdDev;
    const mediumThreshold = avgShortFloat + 0.5 * stdDev;

    // Categorize short interest spikes into different severity levels
    const extremeShortBubbles = [];
    const highShortBubbles = [];
    const mediumShortBubbles = [];

    shortFloatData.forEach((item) => {
      // Create bubble point with proper structure
      const bubblePoint = {
        x: item.time,
        y: item.price,
        z: item.shortFloat, // Size based on short float percentage
        shortFloat: item.shortFloat,
        date: new Date(item.time).toLocaleDateString("en-US", {
          year: "numeric",
          month: "short",
          day: "numeric",
        }),
      };

      // Categorize by severity (keeps same thresholds, but used for grouping/visuals)
      if (item.shortFloat >= extremeThreshold) {
        extremeShortBubbles.push(bubblePoint);
      } else if (item.shortFloat >= highThreshold) {
        highShortBubbles.push(bubblePoint);
      } else if (item.shortFloat >= mediumThreshold) {
        mediumShortBubbles.push(bubblePoint);
      }
    });

    const fillColorStart = "rgba(70, 129, 244, 0.5)";
    const fillColorEnd = "rgba(70, 129, 244, 0.001)";

    const options = {
      credits: { enabled: false },
      chart: {
        backgroundColor: $mode === "light" ? "#fff" : "#09090B",
        plotBackgroundColor: $mode === "light" ? "#fff" : "#09090B",
        height: 400,
        animation: false,
      },
      title: {
        text: `<h3 class="mt-3 -mb-3 text-[1rem] sm:text-lg">Short Interest Activity</h3>`,
        useHTML: true,
        style: { color: $mode === "light" ? "black" : "white" },
      },
      xAxis: {
        type: "datetime",
        crosshair: {
          color: $mode === "light" ? "black" : "white",
          width: 1,
          dashStyle: "Solid",
        },
        labels: {
          style: { color: $mode === "light" ? "#6b7280" : "#fff" },
          formatter: function () {
            const date = new Date(this.value);
            return `<span class="text-xs">${date.toLocaleDateString("en-US", {
              month: "short",
              year: "numeric",
            })}</span>`;
          },
        },
        tickPositioner: function () {
          const positions = [];
          const { min, max } = this.getExtremes();
          const tickCount = 5;
          const interval = Math.floor((max - min) / tickCount);
          for (let i = 0; i <= tickCount; i++) {
            positions.push(min + i * interval);
          }
          return positions;
        },
      },
      yAxis: {
        title: {
          text: "Stock Price ($)",
          style: {
            color: $mode === "light" ? "#6b7280" : "#fff",
          },
        },
        labels: {
          style: {
            color: $mode === "light" ? "#6b7280" : "#fff",
          },
          formatter: function () {
            return `$${this.value.toFixed(2)}`;
          },
        },
        gridLineWidth: 1,
        gridLineColor: $mode === "light" ? "#e5e7eb" : "#111827",
      },
      tooltip: {
        shared: false,
        useHTML: true,
        backgroundColor: "rgba(0, 0, 0, 0.8)",
        borderColor: "rgba(255, 255, 255, 0.2)",
        borderWidth: 1,
        style: {
          color: "#fff",
          fontSize: "14px",
          padding: "10px",
        },
        borderRadius: 4,
        formatter: function () {
          const date = new Date(this.x);
          const formattedDate = date.toLocaleDateString("en-US", {
            month: "short",
            day: "numeric",
            year: "numeric",
          });

          if (this.series.type === "bubble") {
            // Determine how the short float compares to the average
            const val = this.point.shortFloat;
            let severityLabel = "";
            let severityColor = "";

            if (val >= extremeThreshold) {
              severityLabel = "Well above average";
              severityColor = "#dc2626"; // red
            } else if (val >= highThreshold) {
              severityLabel = "Above average";
              severityColor = "#f59e0b"; // orange
            } else if (val >= mediumThreshold) {
              severityLabel = "Slightly above average";
              severityColor = "#fbbf24"; // amber
            } else {
              severityLabel = "Average or below";
              severityColor = $mode === "light" ? "#10b981" : "#34d399"; // green tones
            }

            return `
            <span class="text-white text-sm font-normal">${formattedDate}</span><br>
            <span class="text-white text-sm">Stock Price: $${this.point.y?.toFixed(2)}</span><br>
            <span class="text-white text-sm">Short Float: ${this.point.shortFloat?.toFixed(2)}%</span><br>
            <span class="text-white text-sm">Comparison to Avg: <span style="color: ${severityColor}">${severityLabel}</span></span><br>
          `;
          } else {
            return `
            <span class="text-white text-sm font-normal">${formattedDate}</span><br>
            <span class="text-white text-sm font-[501]">Stock Price: $${this.y?.toFixed(2)}</span>
          `;
          }
        },
      },
      plotOptions: {
        series: {
          legendSymbol: "rectangle",
          animation: false,
          marker: {
            enabled: false,
            states: {
              hover: { enabled: false },
              select: { enabled: false },
            },
          },
        },
        spline: {
          lineWidth: 2.5,
          shadow: false,
        },
        areaspline: {
          lineWidth: 1.5,
          fillOpacity: 0.3,
          shadow: false,
        },
        bubble: {
          minSize: 5, // Smaller minimum for small spikes
          maxSize: 40, // Larger maximum for biggest spikes
          opacity: 0.5,
          marker: {
            enabled: true, // Enable markers for bubbles
            fillOpacity: 0.8,
            lineWidth: 1,
            lineColor: $mode === "light" ? "#d97706" : "#f59e0b",
          },
          dataLabels: {
            enabled: false,
          },
          sizeBy: "z", // Size bubbles by z-value
          zMin: minShortFloat, // Use actual minimum volume for scaling
          zMax: maxShortFloat, // Use actual maximum volume for scaling
          sizeByAbsoluteValue: false, // Use relative sizing for better proportion
        },
      },
      legend: {
        enabled: true,
        align: "center",
        verticalAlign: "top",
        layout: "horizontal",
        squareSymbol: false,
        symbolWidth: 20,
        symbolHeight: 12,
        symbolRadius: 0,
        itemStyle: {
          color: $mode === "light" ? "black" : "white",
        },
      },
      series: [
        {
          name: "Stock Price",
          type: "area",
          data: priceSeries,
          color: "#4681f4",
          lineWidth: 1.5,
          fillColor: {
            linearGradient: { x1: 0, y1: 0, x2: 0, y2: 1 },
            stops: [
              [0, fillColorStart],
              [1, fillColorEnd],
            ],
          },
          animation: false,
          zIndex: 1,
        },
        {
          // points >= avg + 2*stdDev
          name: "Well above average",
          type: "bubble",
          data: extremeShortBubbles,
          color: "#ef4444", // Red for well above avg
          marker: {
            lineColor: "#dc2626",
          },
          animation: false,
          zIndex: 4,
          showInLegend: extremeShortBubbles.length > 0,
        },
        {
          // points >= avg + 1*stdDev
          name: "Above average",
          type: "bubble",
          data: highShortBubbles,
          color: "#fb923c", // Orange for above avg
          marker: {
            lineColor: "#f59e0b",
          },
          animation: false,
          zIndex: 3,
          showInLegend: highShortBubbles.length > 0,
        },
        {
          // points >= avg + 0.5*stdDev
          name: "Slightly above average",
          type: "bubble",
          data: mediumShortBubbles,
          color: "#fbbf24", // Amber for slightly above avg
          marker: {
            lineColor: "#fbbf24",
          },
          animation: false,
          zIndex: 2,
          showInLegend: mediumShortBubbles.length > 0,
        },
      ],
    };

    return options;
  }

  let config = null;

  let columns = [
    { key: "recordDate", label: "Date", align: "left" },
    { key: "totalShortInterest", label: "Short Interest", align: "right" },
    { key: "shortPriorMo", label: "Short Prior Month", align: "right" },
    { key: "percentChangeMoMo", label: "% Change", align: "right" },
    { key: "daysToCover", label: "Day to Cover", align: "right" },
    { key: "shortPercentOfFloat", label: "Short % Float", align: "right" },
    { key: "shortPercentOfOut", label: "Short % Out", align: "right" },
  ];

  let sortOrders = {
    recordDate: { order: "none", type: "date" },
    totalShortInterest: { order: "none", type: "number" },
    shortPriorMo: { order: "none", type: "number" },
    percentChangeMoMo: { order: "none", type: "number" },
    daysToCover: { order: "none", type: "number" },
    shortPercentOfFloat: { order: "none", type: "number" },
    shortPercentOfOut: { order: "none", type: "number" },
  };

  const sortData = (key) => {
    // Reset all other keys to 'none' except the current key
    for (const k in sortOrders) {
      if (k !== key) {
        sortOrders[k].order = "none";
      }
    }

    // Cycle through 'none', 'asc', 'desc' for the clicked key
    const orderCycle = ["none", "asc", "desc"];

    let originalData = rawData?.sort(
      (a, b) => new Date(b?.date) - new Date(a?.date),
    );

    const currentOrderIndex = orderCycle.indexOf(sortOrders[key].order);
    sortOrders[key].order =
      orderCycle[(currentOrderIndex + 1) % orderCycle.length];
    const sortOrder = sortOrders[key].order;

    // Reset to original data when 'none' and stop further sorting
    if (sortOrder === "none") {
      tableList = [...originalData]?.slice(0, 20); // Reset to original data (spread to avoid mutation)
      return;
    }

    // Define a generic comparison function
    const compareValues = (a, b) => {
      const { type } = sortOrders[key];
      let valueA, valueB;

      switch (type) {
        case "date":
          valueA = new Date(a[key]);
          valueB = new Date(b[key]);
          break;
        case "string":
          valueA = a[key].toUpperCase();
          valueB = b[key].toUpperCase();
          return sortOrder === "asc"
            ? valueA.localeCompare(valueB)
            : valueB.localeCompare(valueA);
        case "number":
        default:
          valueA = parseFloat(a[key]);
          valueB = parseFloat(b[key]);
          break;
      }

      if (sortOrder === "asc") {
        return valueA < valueB ? -1 : valueA > valueB ? 1 : 0;
      } else {
        return valueA > valueB ? -1 : valueA < valueB ? 1 : 0;
      }
    };

    // Sort using the generic comparison function
    tableList = [...originalData]?.sort(compareValues)?.slice(0, 20);
  };

  onMount(() => {
    window.addEventListener("scroll", handleScroll);
    return () => {
      window.removeEventListener("scroll", handleScroll);
    };
  });

  $: {
    if ($mode) {
      config = plotData();
    }
  }
</script>

